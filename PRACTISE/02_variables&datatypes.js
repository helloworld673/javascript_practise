let myName="Guni";     //'let' is used to declare a variable in JavaScript;  'let' means the value of the variable CAN change later.
let myAge = 20;        //myAge or myName, etc.. variable names like these known as "camelCase".

//Primitive data types-
// 1. Number
let score = 100;
let temprature = 29.5;
console.log(score);
console.log(temprature);

//2. string
let herName = "Guni";
let college = "SVC";
let sentence = 'hello, my name is Guni';
console.log(herName);
console.log(college);
console.log(sentence);

//3. Boolean
let isStudent = true;
let hasGraduated = false;
console.log(isStudent);
console.log(hasGraduated);

//4. Undefined
let x;
console.log(x);

